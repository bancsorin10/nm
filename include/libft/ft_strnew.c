#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*c;

	i = 0;
	c = (char *)malloc(size + 1);
	if (c == NULL)
		return (NULL);
	while (i <= size)
	{
		c[i] = '\0';
		i++;
	}
	return (c);
}
