#include "libft.h"

char	*ft_strcdup(char *s, char n)
{
	char	*d;
	int		i;

	i = 0;
	if (!(d = (char *)malloc(ft_poschr(s, n) + 1)))
		return (NULL);
	ft_strccpy(d, s, n);
	return (d);
}
