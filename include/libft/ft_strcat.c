#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char *d;

	d = dest;
	while (*d)
		d++;
	while (*src)
	{
		*d = *src;
		d++;
		src++;
	}
	*d = '\0';
	return (dest);
}
